package es.cursillo;

public class Point {
	private Integer x;
	private Integer y;
	private Integer z;
	private Integer w;

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Integer getZ() {
		return z;
	}

	private void setZ(Integer z) {
		this.z = z;
	}

	public Integer getW() {
		return w;
	}

	public void setW(Integer w) {
		this.w = w;
	}

}
